const mongoose = require("mongoose");

const RegSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
    minlength: 6,
  },
  phone: {
    type: String,
    required: true,
    minlength: 9,
  },
});

module.exports = mongoose.model("UserRegistration", RegSchema);
